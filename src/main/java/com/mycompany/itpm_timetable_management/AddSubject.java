    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String year  = jTextField1.getText();
        String semester = jComboBox1.getSelectedItem().toString();
        String sname = jTextField2.getText();
        String scode  = jTextField3.getText();
        String lech = jSpinner1.getValue().toString();
        String tuth = jSpinner2.getValue().toString();
        String labh = jSpinner3.getValue().toString();
        String evoh = jSpinner4.getValue().toString();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
           conn = DriverManager.getConnection(DB_URL, username, password);
           insert = conn.prepareStatement("INSERT INTO subject(Yr,Sem,name,id,lechr,tuthr,labhr,evohr)VALUES(?,?,?,?,?,?,?,?)");
            
            // insert data to relavant ? marks
            insert.setString(1, year);
            insert.setString(2, semester);
            insert.setString(3, sname);
            insert.setString(4, scode);
            insert.setString(5, lech);
            insert.setString(6, tuth);
            insert.setString(7,labh );
            insert.setString(8, evoh);
            
            if(year.isEmpty() || sname.isEmpty() || scode.isEmpty() ){
                JOptionPane.showMessageDialog(this, "All Fields Required!");
            }else{
            // execute the query
            insert.executeUpdate();
            
            // set pop-up message
            JOptionPane.showMessageDialog(this, "Record Added");
            
            // clear text boxes
            jTextField1.setText("");
            jTextField2.setText("");
            
            // focus on first textbox after clearing
            jTextField1.requestFocus();
    }                                        
        }
        catch (ClassNotFoundException ex) {
            Logger.getLogger(AddBuilding.class.getName()).log(Level.SEVERE, null, ex);
            
        } catch (SQLException ex) {
            Logger.getLogger(AddBuilding.class.getName()).log(Level.SEVERE, null, ex);
            }
        

    }//GEN-LAST:event_jButton3ActionPerformed

