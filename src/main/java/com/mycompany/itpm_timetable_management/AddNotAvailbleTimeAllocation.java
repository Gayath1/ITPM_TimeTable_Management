    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        PreparedStatement insert;
        String id = jTextField1.getText();
        String lecname = jComboBox5.getSelectedItem().toString();
        String gid = jComboBox2.getSelectedItem().toString();
        String sgid = jComboBox4.getSelectedItem().toString();
        String Sessionid = jComboBox3.getSelectedItem().toString();
        int sHour = (int) jSpinner1.getValue();
        int eHour = (int) jSpinner3.getValue();
        int sMin = (int) jSpinner2.getValue();
        int eMin = (int) jSpinner4.getValue();
        String ampm = (String) jComboBox6.getSelectedItem();
        String ampm1 = (String) jComboBox7.getSelectedItem();
        String startTime = "";
        String endTime = "";
        
        startTime = sHour+":"+sMin+":00"+ampm;
        endTime = eHour+":"+eMin+":00"+ampm1;

               
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection(DB_URL, username, password);
            insert = conn.prepareStatement("INSERT INTO NotAvailableTime(id,lecturer,Grpid,subgrp,sess,startTime,endTime)VALUES(?,?,?,?,?,?,?)");         
            insert.setString(1, id);
            insert.setString(2, lecname);
            insert.setString(3, gid);
            insert.setString(4, sgid);
            insert.setString(5, Sessionid);
            insert.setString(6, startTime);
            insert.setString(7, endTime);
            
            if(id.isEmpty() || lecname.isEmpty() || gid.isEmpty() || sgid.isEmpty() || Sessionid.isEmpty() || startTime.isEmpty() || endTime.isEmpty()){
                JOptionPane.showMessageDialog(this, "All Fields Required!");
            }else{
            insert.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Added");
            }
        }catch (ClassNotFoundException ex) {
            Logger.getLogger(AddNotAvailbleTimeAllocation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddNotAvailbleTimeAllocation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }                                        

